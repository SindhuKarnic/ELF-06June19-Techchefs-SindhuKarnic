package com.techchefs.javaapp.array;

public class CharArray {

	public static void main(String[] args) {
		char[] ch = new char[6];
		ch[0] = 's';
		ch[1] = 'i';
		ch[2] = 'n';
		ch[3] = 'd';
		ch[4] = 'h';
		ch[5] = 'u';
	
		for(int i =0; i < ch.length; i ++) {
			System.out.println(ch[i]);
		}

	}

}
